/**
 * Code Generator Module
 * 
 * Generates code for:
 * - Service Worker files
 * - Registration code for different frameworks
 */

class CodeGenerator {
  constructor(frameworkInfo, config) {
    this.frameworkInfo = frameworkInfo;
    this.config = config || {};
    this.packageName = '@norejs/prefetch-worker';
    this.esmBase = 'https://esm.sh';
  }
  
  /**
   * Generate complete Service Worker code
   * @param {Object} options
   * @returns {string}
   */
  generateServiceWorker(options = {}) {
    const baseCode = this._generateBaseSW();
    const integrationCode = this.generateIntegrationCode(options);
    
    return baseCode + '\n' + integrationCode;
  }
  
  /**
   * Generate base Service Worker code
   * @private
   */
  _generateBaseSW() {
    return `// Service Worker
// Generated by @norejs/prefetch-migrate

console.log('Service Worker: Script loaded');

// 安装阶段
self.addEventListener('install', (event) => {
  console.log('Service Worker: Installing...');
  self.skipWaiting();
});

// 激活阶段
self.addEventListener('activate', (event) => {
  console.log('Service Worker: Activating...');
  event.waitUntil(
    self.clients.claim().then(() => {
      console.log('Service Worker: Activated and controlling clients');
    })
  );
});

// Fetch 事件处理（将被 Prefetch 集成代码增强）
self.addEventListener('fetch', (event) => {
  // 默认行为：直接透传请求
  // Prefetch 集成代码会拦截并处理匹配的请求
});

console.log('Service Worker: Base setup complete');
`;
  }
  
  /**
   * Generate Prefetch integration code
   * @param {Object} options
   * @returns {string}
   */
  generateIntegrationCode(options = {}) {
    const {
      version = '1.0.0',
      debug = false,
      debugPort = 3100,
      cdnPrefix = null,
      cdnUrl = null
    } = options;
    
    // Resolve CDN URL
    const resolvedCdnUrl = this._resolveCDNUrl({ debug, debugPort, cdnPrefix, cdnUrl, version });
    
    const config = {
      apiMatcher: '/api/*',
      defaultExpireTime: 30000,
      maxCacheSize: 100,
      debug: false,
      ...this.config
    };
    
    const configJson = JSON.stringify(config, null, 2)
      .split('\n')
      .map((line, i) => i === 0 ? line : '      ' + line)
      .join('\n');
    
    return `
// ============================================
// Prefetch Worker Integration
// Version: ${version}
// Mode: ${debug ? 'DEBUG (Local Dev Server)' : 'PRODUCTION (CDN)'}
// Generated: ${new Date().toISOString()}
// ============================================

(function() {
  'use strict';
  
  const PREFETCH_CONFIG = {
    // CDN URL${debug ? ' - Local Dev Server' : ' - esm.sh CDN'}
    cdnUrl: '${resolvedCdnUrl}',
    
    // 本地降级文件（可选）
    fallbackUrl: '/prefetch-worker.umd.js',
    
    // 加载超时时间（毫秒）
    timeout: 5000,
    
    // 最大重试次数
    maxRetries: 2,
    
    // Prefetch 配置
    prefetchConfig: ${configJson}
  };
  
  let prefetchHandler = null;
  let loadAttempts = 0;
  let isLoading = false;
  
  /**
   * 加载 Prefetch Worker
   */
  function loadPrefetchWorker() {
    if (isLoading) {
      console.log('[Prefetch] Already loading...');
      return false;
    }
    
    if (loadAttempts >= PREFETCH_CONFIG.maxRetries) {
      console.error('[Prefetch] Max load attempts reached');
      return false;
    }
    
    isLoading = true;
    loadAttempts++;
    
    try {
      const startTime = Date.now();
      console.log(\`[Prefetch] Loading from CDN (attempt \${loadAttempts})...\`);
      
      // 同步加载脚本
      importScripts(PREFETCH_CONFIG.cdnUrl);
      
      const loadTime = Date.now() - startTime;
      console.log(\`[Prefetch] ✓ Loaded successfully in \${loadTime}ms\`);
      
      // 验证加载成功
      if (!self.PrefetchWorker) {
        throw new Error('PrefetchWorker not found in global scope');
      }
      
      isLoading = false;
      return true;
      
    } catch (error) {
      console.warn(\`[Prefetch] ✗ Load attempt \${loadAttempts} failed:\`, error.message);
      isLoading = false;
      
      // 重试
      if (loadAttempts < PREFETCH_CONFIG.maxRetries) {
        console.log('[Prefetch] Retrying...');
        return loadPrefetchWorker();
      }
      
      // 尝试降级到本地文件
      if (PREFETCH_CONFIG.fallbackUrl) {
        try {
          console.log('[Prefetch] Trying fallback URL...');
          importScripts(PREFETCH_CONFIG.fallbackUrl);
          
          if (self.PrefetchWorker) {
            console.log('[Prefetch] ✓ Loaded from fallback');
            return true;
          }
        } catch (fallbackError) {
          console.error('[Prefetch] ✗ Fallback also failed:', fallbackError.message);
        }
      }
      
      return false;
    }
  }
  
  /**
   * 初始化 Prefetch Handler
   */
  function initializePrefetch(config) {
    if (!self.PrefetchWorker) {
      console.error('[Prefetch] PrefetchWorker not available');
      return null;
    }
    
    try {
      const mergedConfig = {
        ...PREFETCH_CONFIG.prefetchConfig,
        ...config
      };
      
      console.log('[Prefetch] Initializing with config:', mergedConfig);
      
      const handler = self.PrefetchWorker.setup(mergedConfig);
      
      console.log('[Prefetch] ✓ Initialized successfully');
      return handler;
      
    } catch (error) {
      console.error('[Prefetch] ✗ Initialization failed:', error);
      return null;
    }
  }
  
  /**
   * 消息处理 - 支持延迟加载
   */
  self.addEventListener('message', (event) => {
    if (!event.data) return;
    
    // 初始化消息
    if (event.data.type === 'PREFETCH_INIT') {
      console.log('[Prefetch] Received INIT message');
      
      if (!prefetchHandler) {
        // 延迟加载：只在需要时加载
        if (loadPrefetchWorker()) {
          prefetchHandler = initializePrefetch(event.data.config || {});
          
          // 通知主线程初始化完成
          if (event.source && prefetchHandler) {
            event.source.postMessage({
              type: 'PREFETCH_INIT_SUCCESS',
              config: event.data.config
            });
          } else if (event.source) {
            event.source.postMessage({
              type: 'PREFETCH_INIT_ERROR',
              error: 'Failed to initialize Prefetch Worker'
            });
          }
        }
      }
    }
    
    // 健康检查
    if (event.data.type === 'PREFETCH_HEALTH_CHECK') {
      if (event.source) {
        event.source.postMessage({
          type: 'PREFETCH_HEALTH_RESPONSE',
          loaded: !!self.PrefetchWorker,
          initialized: !!prefetchHandler,
          attempts: loadAttempts
        });
      }
    }
  });
  
  /**
   * Fetch 事件处理
   */
  const originalFetchHandler = self.onfetch;
  
  self.addEventListener('fetch', (event) => {
    // 优先使用 Prefetch 处理
    if (prefetchHandler) {
      try {
        const response = prefetchHandler(event);
        if (response) {
          return event.respondWith(response);
        }
      } catch (error) {
        console.error('[Prefetch] Fetch handler error:', error);
      }
    }
    
    // 调用原有的 fetch 处理逻辑（如果存在）
    if (originalFetchHandler) {
      return originalFetchHandler.call(self, event);
    }
  });
  
  console.log('[Prefetch] Integration loaded, waiting for INIT message');
  
})();

// ============================================
// End of Prefetch Worker Integration
// ============================================
`;
  }
  
  /**
   * Resolve CDN URL based on options
   * @private
   */
  _resolveCDNUrl(options) {
    const { debug, debugPort, cdnPrefix, cdnUrl, version } = options;
    
    // 1. Use complete URL if provided
    if (cdnUrl) {
      return cdnUrl;
    }
    
    // 2. Development mode
    if (debug) {
      const port = debugPort || 3100;
      const prefix = cdnPrefix || `http://localhost:${port}`;
      return `${prefix}/prefetch-worker.umd.js`;
    }
    
    // 3. Custom CDN prefix
    if (cdnPrefix) {
      return `${cdnPrefix}/${this.packageName}@${version}/dist/prefetch-worker.umd.js`;
    }
    
    // 4. Default esm.sh CDN
    return `${this.esmBase}/${this.packageName}@${version}/dist/prefetch-worker.umd.js`;
  }
  
  /**
   * Generate registration code for the framework
   * @returns {string}
   */
  generateRegistrationCode() {
    const framework = this.frameworkInfo.name;
    
    switch (framework) {
      case 'nextjs':
        return this._generateNextJSRegistration();
      case 'cra':
      case 'react-vite':
      case 'react':
        return this._generateReactRegistration();
      case 'vue-cli':
      case 'vue-vite':
      case 'vue':
        return this._generateVueRegistration();
      case 'nuxt':
        return this._generateNuxtRegistration();
      default:
        return this._generateGenericRegistration();
    }
  }
  
  /**
   * Generate Next.js registration code
   * @private
   */
  _generateNextJSRegistration() {
    return `// Register Service Worker
if (typeof window !== 'undefined' && 'serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker.register('/service-worker.js')
      .then(registration => console.log('SW registered:', registration))
      .catch(error => console.log('SW registration failed:', error));
  });
}`;
  }
  
  /**
   * Generate React registration code
   * @private
   */
  _generateReactRegistration() {
    return `// Register Service Worker
if ('serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker.register('/service-worker.js')
      .then(registration => console.log('SW registered:', registration))
      .catch(error => console.log('SW registration failed:', error));
  });
}`;
  }
  
  /**
   * Generate Vue registration code
   * @private
   */
  _generateVueRegistration() {
    return `// Register Service Worker
if ('serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker.register('/service-worker.js')
      .then(registration => console.log('SW registered:', registration))
      .catch(error => console.log('SW registration failed:', error));
  });
}`;
  }
  
  /**
   * Generate Nuxt registration code
   * @private
   */
  _generateNuxtRegistration() {
    return `// Register Service Worker
if (process.client && 'serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker.register('/service-worker.js')
      .then(registration => console.log('SW registered:', registration))
      .catch(error => console.log('SW registration failed:', error));
  });
}`;
  }
  
  /**
   * Generate generic registration code
   * @private
   */
  _generateGenericRegistration() {
    return `// Register Service Worker
if ('serviceWorker' in navigator) {
  window.addEventListener('load', () => {
    navigator.serviceWorker.register('/service-worker.js')
      .then(registration => console.log('SW registered:', registration))
      .catch(error => console.log('SW registration failed:', error));
  });
}`;
  }
}

module.exports = CodeGenerator;
